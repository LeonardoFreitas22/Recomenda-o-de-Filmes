# 8. EXECUÇÃO
# ================================
if __name__ == "__main__":

    caminho = r'/content/Top_10000_Movies.csv'

    # Inicializa o recomendador
    sistema = RecomendadorFilmes(caminho)

    # Input do usuário
    filme = input("\nDigite o nome do filme: ")

    # Gera recomendações
    sistema.recomendar(filme, top_n=5)